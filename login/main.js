$('a[href="#signup"]').click(function(e) {
    $('.content').hide();
    $('#signup').show();
});
$('a[href="#login"]').click(function(e) {
    $('.content').hide();
    $('#login').show();
});
$('a[href="#forgot-password"]').click(function(e) {
    $('.content').hide();
    $('#forgot-password').show();
});